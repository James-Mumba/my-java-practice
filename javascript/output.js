/*
VARIABLES
    -Allocation of memory space
    -Hold values upto a certain size
    -Values can change at a later stage of the program
    -Should only be declared once. Never reuse variable names
*/
/* 1. DECLARATION
    - LET keyword
    - VAR keyword
    */
   let a;
   var b;
   console.log(a)
   console.log(b)
/* 2. ASSIGNMENT ( = )
   - We use the equal sign
   - Read as assigned
   */
  let c = 'one';
  var d = 'two';
  console.log(c)
  console.log(d)
/* 3. RE-ASSIGNMENT ( = )
  - We use the equal sign
  - Read as assigned
  */
    c = 'three';
    console.log(c)